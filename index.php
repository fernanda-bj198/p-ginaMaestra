	<h3>
		<br>
		La nanomedicina es una rama de la nanotecnología con aplicaciones directas en medicina, que está permitiendo el abordaje de las enfermedades desde el interior del organismo, a un nivel celular o molecular. De este modo, los dispositivos con un tamaño menor de 50 nm pueden entrar fácilmente en la mayoría de las células mientras que, los menores de 20 nm pueden transitar por el torrente circulatorio.
		La nanomedicina es ya una realidad que está produciendo avances en el diagnóstico, prevención y tratamiento de las enfermedades porque, entre otras razones, permite interactuar con las biomoléculas (proteínas y ácidos nucleicos).
